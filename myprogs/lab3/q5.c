#include<stdio.h>
int main()
{
int u, a, t;
float d, i1, i2;
printf("Please enter initial speed (u): ");
scanf("%d", &u); // stores u
printf("Please enter acceleration (a):");
scanf("%d", &a); // stores a
printf("Please enter time (t):");
scanf("%d", &t); // stores t
i1 = u * t;
i2 = 0.5 * a * t * t;
d = i1 + i2;
printf("The distance covered is: %lf \n", d); // printing the distance
return 0;
}